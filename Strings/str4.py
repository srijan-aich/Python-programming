
name = "srijan"

print (len(name))

print (name.capitalize()) #capitalizes the first letter of the string  
print (name.upper()) #converts the string to uppercase
print (name.lower()) #converts the string to lowercase  
print (name.startswith("s")) #checks if the string starts with a specific substring
print (name.endswith("n")) #checks if the string ends with a specific substring 
print (name.find("j")) #returns the index of the first occurrence of a substring
print (name.replace("s", "S")) #replaces a specific substring with another substring
print (name.count("i")) #returns the number of occurrences of a substring in the string


